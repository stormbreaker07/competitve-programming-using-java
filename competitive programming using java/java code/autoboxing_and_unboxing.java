import java.util.*;


public class autoboxing_and_unboxing {
    
    
    public static void main(String[] args) {

        ArrayList<Integer> Data = new ArrayList<Integer>();
        for(int i=1;i<=10;i++)
        {
            Data.add(i);
        }
        for(int i=1;i<10;i++)
        {
            System.out.println(Data.get(i));
        }


    }



}
